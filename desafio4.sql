SELECT
EMPREGOS.JOB_TITLE AS `Cargo`,
ROUND(AVG(EMPREGADOS.salary),2) AS `Média salarial`, CASE 
WHEN ROUND(AVG(EMPREGADOS.salary),2) >= 2000 AND ROUND(AVG(EMPREGADOS.salary),2) <= 5800 THEN 'Júnior'
WHEN ROUND(AVG(EMPREGADOS.salary),2) >= 5801 AND ROUND(AVG(EMPREGADOS.salary),2) <= 7500 THEN 'Pleno'
WHEN ROUND(AVG(EMPREGADOS.salary),2) >= 7501 AND ROUND(AVG(EMPREGADOS.salary),2) <= 10500 THEN 'Sênior'
ELSE 'CEO'
END AS 'Senioridade'
FROM hr.jobs AS EMPREGOS
INNER JOIN hr.employees AS EMPREGADOS ON EMPREGADOS.job_id = EMPREGOS.job_id
GROUP BY Cargo
ORDER BY `Média salarial` ASC,
Cargo ASC;
