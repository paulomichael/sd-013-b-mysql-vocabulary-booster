SELECT j.JOB_TITLE AS `Cargo`, 
CASE
WHEN e.JOB_ID = 'AC_ACCOUNT' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'AC_MGR' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'AD_ASST' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'AD_PRES' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'AD_VP' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'FI_ACCOUNT' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'FI_MGR' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'HR_REP' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'IT_PROG' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'MK_MAN' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'MK_REP' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'PR_REP' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'PU_CLERK' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'PU_MAN' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'SA_MAN' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'SA_REP' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'SH_CLERK' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'ST_CLERK' THEN ROUND(AVG(e.SALARY), 2)
WHEN e.JOB_ID = 'ST_MAN' THEN ROUND(AVG(e.SALARY), 2)
END AS `Média salarial`,
CASE
WHEN ROUND(AVG(e.SALARY), 2) >= 2000 AND ROUND(AVG(e.SALARY), 2) <= 5800 THEN 'Júnior'
WHEN ROUND(AVG(e.SALARY), 2) >= 5801 AND ROUND(AVG(e.SALARY), 2) <= 7500 THEN 'Pleno'
WHEN ROUND(AVG(e.SALARY), 2) >= 7501 AND ROUND(AVG(e.SALARY), 2) <= 10500 THEN 'Sênior'
WHEN ROUND(AVG(e.SALARY), 2) > 10500  THEN 'CEO'
END AS `Senioridade`
FROM hr.jobs AS j
INNER JOIN hr.employees AS e
ON e.JOB_ID = j.JOB_ID
GROUP BY e.JOB_ID
ORDER BY `Média salarial`, `Cargo`;
