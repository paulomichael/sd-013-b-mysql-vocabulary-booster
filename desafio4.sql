SELECT
	JT.JOB_TITLE AS Cargo,
	ROUND(AVG(JE.SALARY), 2) AS 'Média salarial',
CASE
	WHEN ROUND(AVG(JE.SALARY), 2) BETWEEN 2000 AND 5800 THEN 'Júnior'
	WHEN ROUND(AVG(JE.SALARY), 2) BETWEEN 5801 AND 7500 THEN 'Pleno'
	WHEN ROUND(AVG(JE.SALARY), 2) BETWEEN 7501 AND 10500 THEN 'Sênior'
    WHEN ROUND(AVG(JE.SALARY), 2) > 10500 THEN 'CEO'
END AS Senioridade
FROM hr.jobs as JT
INNER JOIN hr.employees AS JE
ON JT.JOB_ID = JE.JOB_ID
GROUP BY JT.JOB_ID
ORDER BY `Média salarial`, Cargo;