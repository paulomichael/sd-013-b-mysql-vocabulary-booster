SELECT
    jo.JOB_TITLE,
	AVG(SALARY) as "Média salarial",
    CASE
	  WHEN AVG(SALARY) BETWEEN 2000 AND 5800 <= 10000 THEN 'Júnior'
	  WHEN AVG(SALARY) BETWEEN 5801 AND 7500 THEN 'Pleno'
	  WHEN AVG(SALARY) BETWEEN 7501 AND 10500 THEN 'Sênior'
	  WHEN AVG(SALARY) > 10500 THEN 'CEO'
	  ELSE null
	END as "Senioridade"
FROM hr.employees as em
INNER JOIN hr.jobs as jo
ON em.JOB_ID = jo.JOB_ID
GROUP BY em.JOB_ID
ORDER BY AVG(SALARY), JOB_TITLE ASC;
