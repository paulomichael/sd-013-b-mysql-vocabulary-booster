SELECT UCASE(CONCAT(E.FIRST_NAME, " ", E.LAST_NAME)) AS `Nome Completo`, JH.START_DATE AS `Data de início no cargo`, E.SALARY AS Salário
FROM hr.employees AS E
INNER JOIN job_history AS JH
ON E.EMPLOYEE_ID = JH.EMPLOYEE_ID
WHERE MONTH(START_DATE) = 1 OR MONTH(START_DATE) = 2 OR MONTH(START_DATE) = 3
ORDER BY `Nome Completo`, START_DATE;
