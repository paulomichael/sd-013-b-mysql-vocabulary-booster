USE hr;
SELECT JOB_TITLE AS Cargo,
	CASE
		WHEN MAX_SALARY >= 5000 AND MAX_SALARY <= 1000 THEN 'Baixo'
		WHEN MAX_SALARY >= 1001 AND MAX_SALARY <= 20000 THEN 'Médio'
        WHEN MAX_SALARY >= 20001 AND MAX_SALARY <= 30000 THEN 'Alto'
        WHEN MAX_SALARY > 30000 THEN 'Altíssimo'
	END AS Nível
FROM jobs
ORDER BY Cargo;