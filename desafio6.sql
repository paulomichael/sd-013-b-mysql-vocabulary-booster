SELECT
  CONCAT(EMPLOYEES.FIRST_NAME, ' ', EMPLOYEES.LAST_NAME) AS `Nome completo`,
  JOBS.JOB_TITLE AS `Cargo`,
  HISTORY.START_DATE AS `Data de in√≠cio do cargo`,
  DEPARTMENT.DEPARTMENT_NAME AS `Departamento`
FROM
  hr.job_history AS HISTORY
  INNER JOIN hr.employees AS EMPLOYEES ON EMPLOYEES.EMPLOYEE_ID = HISTORY.EMPLOYEE_ID
  INNER JOIN hr.jobs AS JOBS ON JOBS.JOB_ID = EMPLOYEES.JOB_ID
  INNER JOIN hr.departments AS DEPARTMENT ON DEPARTMENT.DEPARTMENT_ID = HISTORY.DEPARTMENT_ID
ORDER BY
  `Nome completo` DESC,
  `Cargo`;
