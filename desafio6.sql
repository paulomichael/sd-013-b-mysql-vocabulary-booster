SELECT CONCAT(EMPLOYEES.FIRST_NAME, ' ', EMPLOYEES.LAST_NAME) AS `Nome completo`,
JOBS.job_title AS `Cargo`,
job_history.start_date AS 'Data de in√≠cio do cargo',
DEPARTAMENTOS.department_name AS 'Departamento'

FROM hr.job_history AS JOB_HISTORY
INNER JOIN hr.employees AS EMPLOYEES
ON JOB_HISTORY.EMPLOYEE_ID = EMPLOYEES.EMPLOYEE_ID

INNER JOIN hr.jobs AS JOBS ON JOBS.JOB_ID = JOB_HISTORY.JOB_ID
INNER JOIN hr.departments AS DEPARTAMENTOS ON DEPARTAMENTOS.DEPARTMENT_ID = JOB_HISTORY.DEPARTMENT_ID
ORDER BY `Nome completo` DESC, Cargo;
